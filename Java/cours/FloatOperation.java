public class FloatOperation {
    
    // Méthode pour ajouter deux nombres à vergules
    public static float addTwoFloats(float a, float b) {
        return a + b; // Retourne la somme des deux nombres
    }
    
    // Méthode pour diviser deux nombres vergules
    public static float divideTwoFloats(float a, float b) {
        return a / b; // Retourne le résultat de la division
    }
}
