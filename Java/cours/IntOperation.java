public class IntOperation {
    
    // Méthode pour additionner deux entiers
    public static int addTwoIntegers(int a, int b) {
        return a + b; // Retourne la somme des deux entiers
    }
    
    // Méthode pour soustraire deux entiers
    public static int subtractTwoIntegers(int a, int b) {
        return a - b; // Retourne la différence entre les deux entiers
    }
    
    // Méthode pour multiplier deux entiers
    public static int multiplyTwoIntegers(int a, int b) {
        return a * b; // Retourne le produit des deux entiers
    }
    
    // Méthode pour diviser deux entiers
    public static int divideTwoIntegers(int a, int b) {
        return a / b; // Retourne le quotient de la division entière
    }
}
